    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            String s = id.getText();
            int expense_id = Integer.parseInt(s);
            try {
                Connection connection = Database.getConnection();
                Statement statement = Database.getStatement(connection);

                new ExpenseQuery().delete(expense_id, statement);
                JOptionPane.showMessageDialog(null, "SuccessFully Deleted");
                id.setText("");

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                setTable();

            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (Exception e) {
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please select an item!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        id.setText(model.getValueAt(selectedRowIndex, 1).toString());
        amount = Double.parseDouble(model.getValueAt(selectedRowIndex, 2).toString());
        cat = model.getValueAt(selectedRowIndex, 3).toString();
        date = model.getValueAt(selectedRowIndex, 4).toString();
    }//GEN-LAST:event_jTable1MouseClicked

